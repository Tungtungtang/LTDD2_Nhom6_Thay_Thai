package com.example.trandangkhoa_17211tt1738;

public class HinhAnh {
    int dau;
    int than;
    int chan;
    public HinhAnh() {
    }

    public HinhAnh(int dau, int than, int chan) {
        this.dau = dau;
        this.than = than;
        this.chan = chan;
    }

    public int getDau() {
        return dau;
    }

    public void setDau(int dau) {
        this.dau = dau;
    }

    public int getThan() {
        return than;
    }

    public void setThan(int than) {
        this.than = than;
    }

    public int getChan() {
        return chan;
    }

    public void setChan(int chan) {
        this.chan = chan;
    }
}
